    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed

    String titulo = txtTitulo.getText().trim();
    String idTexto = idtxt.getText().trim();

    // Validar que no est√©n vac√≠os
    if (titulo.isEmpty() || idTexto.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, ingresa el ID y el t√≠tulo.");
        return;
    }

    try {
        int id = Integer.parseInt(idTexto); // Convertir el ID a n√∫mero

        // Crear un nuevo libro y agregarlo a la biblioteca
        Libro nuevoLibro = new Libro(id, titulo);
        biblioteca.agregarLibro(nuevoLibro);

        JOptionPane.showMessageDialog(this, "Libro agregado exitosamente.");

        // üîπ Llamar a VentanaPrincipal para actualizar la tabla
        if (getParent() instanceof VentanaPrincipal) {
            ((VentanaPrincipal) getParent()).llenarTablaLibros();
        }

        this.dispose(); // Cerrar la ventana despu√©s de agregar

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "El ID debe ser un n√∫mero v√°lido.");
}

    }//GEN-LAST:event_btnAgregarActionPerformed

